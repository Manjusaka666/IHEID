function [lhs, rhs] = static_resid(y, x, params)
T = NaN(2, 1);
lhs = NaN(15, 1);
rhs = NaN(15, 1);
T(1) = (1-params(10))/params(9);
T(2) = (1-params(5))/params(5)*(1-params(5)*params(2));
