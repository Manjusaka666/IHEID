lhs(4) = y(4);
rhs(4) = y(9)+(1-params(1))*y(5);
lhs(5) = y(7)-y(7)*params(3);
rhs(5) = T(2)*(y(2)*params(8)-(1+params(8))*x(1)+params(1)*y(5)+(y(1)-y(1)*params(10))*T(1))+(y(7)-y(7)*params(3))*params(2);
lhs(6) = y(8)-y(8)*params(4);
rhs(6) = y(9)*(1-params(6))/params(6)*(1-params(2)*params(6))+params(2)*(y(8)-y(8)*params(4))+x(5);
lhs(7) = y(6);
rhs(7) = y(7);
lhs(8) = y(3)-y(6)-(y(13)-y(12));
rhs(8) = (-(params(11)*y(10)))-x(4);
lhs(9) = y(1)+y(10);
rhs(9) = y(2)+y(10)/params(2)-params(1)*(y(5)+y(9));
lhs(10) = y(3);
rhs(10) = y(3)*params(12)+y(6)*params(13)+y(2)*params(14)+x(2);
lhs(11) = y(11);
rhs(11) = y(11)*params(21)+params(25)*x(6);
lhs(12) = y(12);
rhs(12) = y(12)*params(22)+params(24)*x(7);
lhs(13) = y(13);
rhs(13) = y(13)*params(23)+params(26)*x(8);
lhs(14) = y(14);
rhs(14) = y(12)+y(14)-y(6);
lhs(15) = y(15);
rhs(15) = x(3);
end
